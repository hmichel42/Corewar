#include "../inc/libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*next;
	t_list	*new;

	new = *alst;
	if (alst != NULL)
	{
		while (new != NULL)
		{
			next = new->next;
			del(new->content, new->content_size);
			free(new);
			new = next;
		}
		*alst = NULL;
	}
}
